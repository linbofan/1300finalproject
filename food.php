	<h1>Apple Festival Dining and Treats!</h1>
	<p>The Apple Harvest Festival obviously specializes in Apple Cuisine and features lots and lots of creative dining options</p>
	<p>Over one hundred vendors sold unique apple varieties, some of which were engineered and produced by Cornell faculty and students. Wineries and cideries also provide their regional beverages for tasting and purchasing. The cideries in particular sell a great deal of cider during Applefest, because their product tends to be associated with the Fall, and harvest season. Many cideries are highly innovative, and combine other regional and exotic fruits to give their ciders unique tastes and aromas. </p>
    <p>International cuisine is also available, from countries as diverse as Cuba and Vietnam. The Applefest, clearly a revered Ithaca tradition, celebrated its thirty fourth year and took place from September 30th to October 2nd. The event prides itself on the family friendly atmosphere. There are numerous activities for children, one in particular allows children to visit with goats and learn how fiber is made courtesy of the Laughing Goat Fiber Farm. The cider donuts and slushies generally tend to be a big hit with kids and elders alike-itâ€™s easy to see why.</p>
